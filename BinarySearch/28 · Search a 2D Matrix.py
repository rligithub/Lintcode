class Solution:
    def searchMatrix(self, matrix, target):
        if matrix == None or len(matrix) == 0:
            return False
        n, m = len(matrix), len(matrix[0])
        x, y = 0, m - 1

        while x <= n - 1 and y >= 0:
            print(x, y)
            goal = matrix[x][y]
            if target > goal:
                x += 1
            if target < goal:
                y -= 1
            if target == goal:
                return True
        return False

